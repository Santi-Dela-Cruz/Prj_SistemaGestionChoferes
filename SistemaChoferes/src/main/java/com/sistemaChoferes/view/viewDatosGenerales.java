    private void consultarTabla(int idc) {
        String sql = "SELECT * FROM registro_Estados WHERE id_Chofer = ?";
        sql = sql.replace("?", String.valueOf(idc));
        
        try {
            connection = conexion.conectar();
            st = connection.createStatement();
            rs = st.executeQuery(sql);
            Object[] datosChoferes = new Object[5];
            defTableMod = (DefaultTableModel) tbEstadoChofer.getModel();
            defTableMod.setRowCount(0);
            while (rs.next()) {
                datosChoferes[0] = rs.getString("fecha_Ingreso");
                datosChoferes[1] = rs.getString("hora_Ingreso");
                datosChoferes[2] = rs.getString("estado_Chofer");
                datosChoferes[3] = rs.getString("autorizacion_Chofer");

                defTableMod.addRow(datosChoferes);
            }
            tbEstadoChofer.setModel(defTableMod);
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) rs.close();
                if (st != null) st.close();
                if (connection != null) connection.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }
    
    public void mostarDatos(int idc){
        
        Chofer chofer = chofeDAO.obtenerChoferPorId(idc);
        Vehiculo vehiculo = vehiculoDAO.obtenerVehiculoPorChoferId(idc);
        Huella huella = huellaDAO.obtenerHuellaPorId(idc);
        Rutas rutas = rutasDAO.obtenerRutaPorChoferId(idc);
        
        Date fecha = huella.getFechaRegHuella();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
        String fechaIngreso = sdf.format(fecha);
        
        if (chofer != null) {
            lbLlenoCedula.setText(chofer.getIdCedula());;
            lbLlenoEstado.setText("Activo");;
            lbLlenoNombres.setText(chofer.getNombre());;
            lbLlenoApellidos.setText(chofer.getApellido());;
            lbLlenoTelefono.setText(chofer.getTelefono());;
            lbLlenoPlaca.setText(vehiculo.getIdPlaca());;
            lbLlenoRuta.setText(rutas.getNombreRuta());;
            lbLlenoFechaIn.setText(fechaIngreso);
            lbLlenoFechaMod.setText(fechaIngreso);
            lbLlenoHuella.setText(huella.getIdCodigoHuella());;
        } else {
            JOptionPane.showMessageDialog(this, "Error al cargar los datos del chofer", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
